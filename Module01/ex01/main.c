#include <avr/io.h>

int main( void )
{
	unsigned long i;

	DDRB |= ( 1 << PB1 ); // Configure PB1 as an output

	while ( 1 )
	{
		PORTB ^= ( 1 << PB1 ); // Switch LED state

		for ( i = 0; i < 2000000; i += 1 );
	}

	return ( 0 );
}
